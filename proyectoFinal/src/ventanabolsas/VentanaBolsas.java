    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:

        if (jTextField1.getText().isEmpty()
                || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()
                || jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos obligatorios");
            jButton2.setEnabled(false);
        }

        Bolsas nuevo = new Bolsas();

        nuevo.setColor((jTextField1.getText()));
        nuevo.setModelo((String) jComboBox2.getSelectedItem());
        nuevo.setMaterial(jTextField3.getText());
        nuevo.setMarca(jTextField4.getText());
        nuevo.setNumcompar(Integer.parseInt((String) jComboBox1.getSelectedItem()));
        nuevo.setPrecio(Float.parseFloat(jTextField6.getText()));
        nuevo.setAza((String) jComboBox3.getSelectedItem());

        modelo.agregarBolsas(nuevo);
    }//GEN-LAST:event_jButton2MouseClicked
